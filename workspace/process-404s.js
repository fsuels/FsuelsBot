const redirects = new Map(); // Use Map to deduplicate by "from" path
let skipped = 0;

for (const fullUrl of rawUrls) {
  let fromPath;
  try {
    const u = new URL(fullUrl);
    fromPath = u.pathname;
    // Shopify redirects only work with path (no query string)
    // But we need to include the path as-is
  } catch(e) {
    skipped++;
    continue;
  }

  const target = getRedirectTarget(fullUrl);
  if (!target) {
    skipped++;
    continue;
  }

  // Don't redirect to self
  if (fromPath === target) {
    skipped++;
    continue;
  }

  // Don't include if already have this redirect from path
  if (!redirects.has(fromPath)) {
    redirects.set(fromPath, target);
  }
}

