function getRedirectTarget(fullUrl) {
  let path;
  try {
    const u = new URL(fullUrl);
    path = u.pathname;
    // Strip query params and variants for the redirect path (Shopify redirects don't support query params)
  } catch(e) {
    return null;
  }

  // Skip invalid/junk URLs
  if (path.includes('wpm@') || path.includes('sandbox/') || path.includes('*/cart') || path === '/b' || path === '/comments' || path === '/thank_you' || path.includes('paginfo@') || path.includes('/a/s/')) {
    return '/collections/all';
  }

  // Strip locale prefix: /en-XX/...
  const localeMatch = path.match(/^\/en-[a-z]{2}(\/.*)?$/);
  let strippedPath = path;
  if (localeMatch) {
    strippedPath = localeMatch[1] || '/';
  }

  // Pattern: /collections/COLLECTION/products/PRODUCT  /products/PRODUCT
  const collProdMatch = strippedPath.match(/^\/collections\/[^\/]+\/products\/([^?]+)/);
  if (collProdMatch) {
    return '/products/' + collProdMatch[1];
  }

  // Pattern: /products/PRODUCT (with or without variant)
  const prodMatch = strippedPath.match(/^\/products\/([^?]+)/);
  if (prodMatch) {
    return '/products/' + prodMatch[1];
  }

  // Pattern: /collections/COLLECTION
  const collMatch = strippedPath.match(/^\/collections\/([^?]+)/);
  if (collMatch) {
    const collSlug = collMatch[1].replace(/\?.*$/, '').replace(/\/+$/, '');
    if (collectionMapping[collSlug]) {
      return collectionMapping[collSlug];
    }
    return '/collections/' + collSlug;
  }

  // Pattern: /pages/PAGE
  const pageMatch = strippedPath.match(/^\/pages\/([^?]+)/);
  if (pageMatch) {
    return '/pages/' + pageMatch[1];
  }

  // Pattern: /account/register
  if (strippedPath.includes('/account/register')) {
    return '/account/register';
  }

  // Root locale pages
  if (strippedPath === '/' || strippedPath === '') {
    return '/';
  }

  // Anything else  /collections/all
  return '/collections/all';
}

